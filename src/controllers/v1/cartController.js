const { cartServices } = require('../../services/v1');

const cartService = cartServices;

module.exports = cartController = {
  getCartUser: async (req, res) => {
    const query = await cartService.getCart(req.decoded);
    if (query) {
      if (!query.code) {
        return res.status(500).json({
          code: 500,
          message: 'Internal server error',
        });
      }
      return res.status(query.code).json(query);
    }
    res.status(500).json({
      code: 500,
      message: 'Internal server error',
    });
  },
  addCartGames: async (req, res) => {
    const { id } = req.params;
    const query = await cartService.addItem(id, req.decoded);
    if (query) {
      if (!query.code) {
        return res.status(500).json({
          code: 500,
          message: 'Internal server error',
        });
      }
      return res.status(query.code).json(query);
    }
    res.status(500).json({
      code: 500,
      message: 'Internal server error',
    });
  },
  removeCartGames: async (req, res) => {
    const { id } = req.params;
    const query = await cartService.removeItem(id, req.decoded);
    if (query) {
      if (!query.code) {
        return res.status(500).json({
          code: 500,
          message: 'Internal server error',
        });
      }
      return res.status(query.code).json(query);
    }
    res.status(500).json({
      code: 500,
      message: 'Internal server error',
    });
  },
};
